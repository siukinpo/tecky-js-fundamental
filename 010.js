const students1 = ["Gordon", "Alex", "Jason", "Joan", "Tracy", "Michael"];
console.log(students1);
students1.splice(2, 3); // 由index 2開始嗰個string開始殺走3個
console.log(students1);
students1.reverse();
console.log(students1);
students1.sort(); //將佢哋排次序 跟英文字母
console.log(students1);
students1.reverse();
console.log(students1);

